public class over2 {
    public static int sum(int a, int b){
        return a+b;
    }
    public static float sum(float c,float d){
        return c+d;
    } 
    public static void main(String[] args) {
        System.out.println(sum(4,5));
        System.out.println(sum(0.4f,0.5f));
        
    }
    
}
